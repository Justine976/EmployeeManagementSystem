    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        if (isMax) {
            isMax = false;
            this.setPreferredSize(new java.awt.Dimension(50, 0));
            dashboardButton.setText("");
            addEmployeeButton.setText("");
            viewEmployeesButton.setText("");
            attendanceListButton.setText("");
        } else {
            isMax = true;
            this.setPreferredSize(new java.awt.Dimension(220, 0));
            dashboardButton.setText("DASHBOARD");
            addEmployeeButton.setText("ADD EMPLOYEE");
            viewEmployeesButton.setText("VIEW EMPLOYEES");
            attendanceListButton.setText("ATTENDANCE LIST");
        }

        this.revalidate();
        this.repaint();
    }//GEN-LAST:event_jLabel2MouseClicked


