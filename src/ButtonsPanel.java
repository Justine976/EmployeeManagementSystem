        jLabel2.setText("");
        jLabel2.setIcon(new ImageIcon(menuImg));

        String dashboardIcon = isDark ? "dashboardIcon-for-dark.png" : "dashboardIcon-for-light.png";
        dashboardButton.setIcon(new ImageIcon(new ImageIcon(getClass().getResource("/resources/" + dashboardIcon)).getImage().getScaledInstance(30, 30, Image.SCALE_SMOOTH)));

        String addEmployeeIcon = isDark ? "addEmployeeIcon-for-dark.png" : "addEmployeeIcon-for-light.png";
        addEmployeeButton.setIcon(new ImageIcon(new ImageIcon(getClass().getResource("/resources/" + addEmployeeIcon)).getImage().getScaledInstance(30, 30, Image.SCALE_SMOOTH)));

        String employeeListIcon = isDark ? "employeeListIcon-for-dark.png" : "employeeListIcon-for-light.png";
